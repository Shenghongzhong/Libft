#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	const char	*s;
	char		*d;
	size_t		i;

	i = 0;
	s = src;
	d = dest;
	while (i < n && s && d)
	{
		d[i] = s[i];
		i++;
	}
	return (dest);
}
