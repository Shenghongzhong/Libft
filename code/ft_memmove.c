#include "libft.h"

void	*ft_memmove(void *dest, void const *src, size_t n)
{
	char	*d;
	char const	*s;

	d = (char *)dest;
	s = (const char *)src;
	if (d == s || n == 0)
		return (d);
	if (d < s)
	{
		while (n--)
			*d++ = *s++;
	}
	else
	{
		d += n;
		s += n;
		while (n--)
			*(--d) = *(--s);
	}
	return (dest);

}
