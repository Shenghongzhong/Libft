int	ft_isalnum(int c)
{
	int	result;

	result = 0;
	if (c >= 'a' && c <= 'z')
		result = 1;
	else if (c >= 'A' && c <= 'Z')
		result = 1;
	else if (c >= 48 && c <= 57)	
		result = 1;
	return (result);
}
